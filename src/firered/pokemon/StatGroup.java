package firered.pokemon;

public class StatGroup {
	public int hp, att, def, spatt, spdef, speed;

	public StatGroup(int hp, int att, int def, int spatt, int spdef, int speed) {
		this.hp = hp;
		this.att = att;
		this.def = def;
		this.spatt = spatt;
		this.spdef = spdef;
		this.speed = speed;
	}
}